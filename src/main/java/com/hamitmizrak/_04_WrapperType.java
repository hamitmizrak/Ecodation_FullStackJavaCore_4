package com.hamitmizrak;

public class _04_WrapperType {

    static void main() {
        // Wrapper type(Sarmalayıcı Türler)
        // heap memory
        // null değer vebilirsiniz
        // OOP en yakın olandır.

        // 4 tane sayı
        Byte wrapperByte=127; // -128<=SAYI<=127
        Short wrapperShort=300;
        Integer wrapperInt=511565115;
        Long wrapperLong=5115651156L;


        // 2 tane virgüllü(Floating point)
        Float wrapperFloat=14.53F;
        Double wrapperDouble=1453.10711071;

        // boolean (true,false)
        Boolean wrapperBoolean=12>15;  //true;

        // char
        Character wrapperChar='&';

        // null verebilirsiniz
        Byte data=null;
    }
}
