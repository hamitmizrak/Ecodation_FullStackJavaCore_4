package com.hamitmizrak.course_1;

public class _03_PrimitiveType {
    static void main() {
        // primitive type(İlkel Türler)
        // stack memory
        // null değer alamaz
        // 8 tanedir

        // 4 tane sayı
        byte primitiveByte=127; // -128<=SAYI<=127
        short primitiveShort=300;
        int primitiveInt=511565115;
        long primitiveLong=5115651156L;

        // 2 tane virgüllü(Floating point)
        float primitiveFloat=14.53F;
        double primitiveDouble=1453.10711071;

        // boolean (true,false)
        boolean primitiveBoolean=12>15;  //true;

        // char
        char primitiveChar='&';
        System.out.println(primitiveChar);

        char primitiveChar2='\u4235';
        System.out.println(primitiveChar2);

        // null veremezsiniz
        //int data=null;
    }

}
