package com.hamitmizrak.course_1;

public class _02_Variable {

    static void main() {
        int schoolNumber=44;
        System.out.println(schoolNumber);

        // Dikkat yazılabilir

        // underscore(alt çizgi)
        int _schoolNumber=23;
        System.out.println(_schoolNumber);

        // Dolar sembolü
        int $schoolNumber=23;
        System.out.println($schoolNumber);

        // sayı ile başlama, özel simge ile başlama
        // üğşçöİ => başlama
    }
}
