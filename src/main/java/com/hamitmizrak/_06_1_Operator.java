package com.hamitmizrak;

public class _06_1_Operator {

    static void main() {
        // + - * /
        System.out.println("Toplama: "+(9+3));
        System.out.println("Çıkarma: "+(9-3));
        System.out.println("Çarpma: "+(9*3));
        System.out.println("Bölme: "+(9/3));
        System.out.println("Kalan: "+(9%3));

        // Mantıksal Operator
        // | ||  ==> VEYA
        // & &&  ==> VE
    }
}
