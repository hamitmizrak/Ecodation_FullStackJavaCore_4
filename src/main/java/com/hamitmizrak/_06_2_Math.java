package com.hamitmizrak;

// Mathematics
// static düzenlidir
public class _06_2_Math {

    static void main() {
        System.out.println(Math.PI);
        System.out.println(Math.E);
        System.out.println("Karekök: "+Math.sqrt(16));
        System.out.println("Üslü : "+Math.pow(2,5));
        System.out.println("Mutlak : "+Math.abs(-10));
        System.out.println("Min : "+Math.min(10,200));
        System.out.println("Max : "+Math.max(10,200));
        System.out.println("alta yuvarla : "+Math.floor(5.9));
        System.out.println("yukarı yuvarla : "+Math.ceil(5.1));
        System.out.println("round-1 : "+Math.round(5.4));
        System.out.println("round-2 : "+Math.round(5.5));

        System.out.println("sinüs : "+Math.sin(45));
        System.out.println("cosinüs : "+Math.cos(45));
        System.out.println("tan : "+Math.tan(45));
        System.out.println("asinüs : "+Math.asin(-1));
    }
}
